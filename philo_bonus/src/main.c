#include "../include/philo.h"

int	main(int argc, char **argv)
{
	t_sim	sim;

	memset(&sim, 0, sizeof(t_sim));
	validate_input(&sim, argc, argv);
	init_config(&sim, argc, argv);
	create_children(&sim);
	wait_for_children(&sim);
	cleanup(&sim);
	printf("End of simulation\n");
	return (EXIT_SUCCESS);
}
